package sec03.exam03;

public class ByteOperationExample {
	public static void main(String[] args) {
		byte result1 = 10 + 20;
		System.out.println(result1);
    // byte 계산
		
		byte x = 10;
		byte y = 20;
		int result2 = x + y;
    // byte 값들을 int로 자동 변환시킨 후 계산
		System.out.println(result2);
	}
}

// 출력 예시
// 30
